@app.on_message(filters.command(["logo"]))
async def lllego(client, message):
    quew = message.text.split(maxsplit=1)[1] if len(message.command) > 1 else ""

    if message.from_user.id != OWNER_ID and not quew:
        await message.reply_text(
            "`Give some text to create a logo !`\n`Example /logo ZeroTwo"
        )
        return
    
    # if 12 ltrs logo not acha 
    if message.from_user.id != OWNER_ID and len(quew) > 12:
        x = await message.reply_text("warning warning")
        await asyncio.sleep(3)
        await x.delete()
     #   return
        pass
    
    
    pesan = await message.reply_text("**Creating your requested logo please wait a sec...**")
    try:
        text = quew
        randc = random.choice(LOGO_LINKS)
        img = Image.open(io.BytesIO(requests.get(randc).content))
        draw = ImageDraw.Draw(img)
        image_widthz, image_heightz = img.size
        fnt = glob.glob("./assets/fonts/*")
        randf = random.choice(fnt)
        font = ImageFont.truetype(randf, 120)
        w, h = draw.textsize(text, font=font)
        h += int(h * 0.21)
        image_width, image_height = img.size
        draw.text(
            ((image_widthz - w) / 2, (image_heightz - h) / 2),
            text,
            font=font,
            fill=(255, 255, 255),
        )
        x = (image_widthz - w) / 2
        y = (image_heightz - h) / 2 + 6
        draw.text(
            (x, y), text, font=font, fill="white", stroke_width=1, stroke_fill="black"
        )
        fname = "shivanilogo.png"
        img.save(fname, "png")
        await message.reply_photo(
            photo=fname,
            caption=f"Logo generated by {app.mention}",
        )
        await message.delete()
        await pesan.delete()
        if os.path.exists(fname):
            os.remove(fname)
    except Exception as e:
        print(e)
        return 


